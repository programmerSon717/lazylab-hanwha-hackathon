function matchProduct(text) {
  const t = (text || "").toLowerCase();
  let best = null;
  let bestScore = 0;
  for (const p of PRODUCTS) {
    // 맞은 키워드 개수로 점수 → 스치는 1회 오매칭이 전체를 좌우하지 않게 한다
    const score = p.keywords.reduce(
      (n, k) => (t.includes(k.toLowerCase()) ? n + 1 : n),
      0
    );
    if (score > bestScore) {
      bestScore = score;
      best = p;
    }
  }
  return bestScore > 0 ? best : DEFAULT_PRODUCT;
}
